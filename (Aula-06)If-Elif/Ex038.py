# Comparando numeros
print('COMPARADOR DE VALORES:\n')

num1 = int(input('Digite o 1º valor: '))
num2 = int(input('Digite o 2º valor: '))

print('\nResultado:')

if num1 > num2:
    print(f'{num1} é o maior valor!')

elif num2 > num1:
    print(f'{num2} é o maior valor!')

else:
    print('Os valores são iguais!!')